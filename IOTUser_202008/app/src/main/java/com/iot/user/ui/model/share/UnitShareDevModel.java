package com.iot.user.ui.model.share;

public class UnitShareDevModel {
    public UnitShareDevModel(){

    }
    private String devName;
    private String devDefaultName;
    private String devNum;

    public String getDevName() {
        return devName;
    }

    public void setDevName(String devName) {
        this.devName = devName;
    }

    public String getDevDefaultName() {
        return devDefaultName;
    }

    public void setDevDefaultName(String devDefaultName) {
        this.devDefaultName = devDefaultName;
    }

    public String getDevNum() {
        return devNum;
    }

    public void setDevNum(String devNum) {
        this.devNum = devNum;
    }
}
